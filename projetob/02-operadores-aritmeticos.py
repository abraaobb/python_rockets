a = 10
b = 10

# soma
soma = a + b

# subtracao
subtracao = a - b

# multiplicacao
multiplicacao = a * b

# divisao
divisao = a / b

print('{} + {} = {}'.format(a, b, soma))
print('{} - {} = {}'.format(a, b, subtracao))
print('{} * {} = {}'.format(a, b, multiplicacao))
print('{} / {} = {}'.format(a, b, divisao))
